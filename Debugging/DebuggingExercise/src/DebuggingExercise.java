
public class DebuggingExercise {
    public static void main(String[] args) {

        // declare a variable called temperature and initialise with 0 degree celsius
        int temperature = 30;
        // print to output
        System.out.println("Current temperature is: " + temperature + " degree celsius");

        // A simple if else statement. You don't have to understand now as we will cover this in later week.
        // If the condition is true, it will print "Temperature is hot". Otherwise, it will print "Temperature is cold"
        if (temperature > 25)
            System.out.println("Temperature is hot");
        else
            System.out.println("Temperature is cold");

    }
}
